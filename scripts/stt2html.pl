(
 input_layer => '',
 path        => 'htdocs/assets/templates/graph/easy/marpa',
);

my($column, @column);
my(@row);

for $column (@heading)
{
	push @column, {td => $column};
}

push @row, [@column];

for my $item (@$stt)
{
	@column = ();

	for $column (@heading)
	{
		push @column, {td => mark_raw($$item{$column} || '.')};
	}

	push @row, [@column];
}

@column = ();

for $column (@heading)
{
	push @column, {td => $column};
}

push @row, [@column];

my($table) =
{
	border  => 0,
	row     => [@row],
	size    => $#row + 1,
	summary => 'STT',
};

print $templater -> render
(
 'rainbow.page.tx',
 {
	 title => 'State Transition Table for Graph::Easy::Marpa::Lexer',
	 table => $table,
 },
);
